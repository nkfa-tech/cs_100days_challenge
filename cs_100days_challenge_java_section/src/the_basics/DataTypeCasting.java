package the_basics;

public class DataTypeCasting {
    public static void main(String[] args) {

        int a = 10; // 32bit
        double b = a ; // 64bit - wide casting

        int c = (int) b ; // အကြီးက‌ေကနေ အငယ်ကို ‌ေ ပေပြောင်းချင် ရင် casting လုပ်‌ေပ်ပေးရတယ် - narrowing casting

        // ‌ေ ဒေသာ အ‌ေအပေပြောင်းလဲ လုပ်‌ေပ်ပေးတာကို casting လုလို့ ‌ေ ခေါတယ်
    }
}
